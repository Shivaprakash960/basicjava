class Paint{

	static int paintId;
	static String brandName;
	static String color;
	
	static void setPaintId(int pId){
		paintId=pId;
	}
	static int getPaintId(){
	return paintId;
	}
	
	static void setBrandName(String bd){
		brandName=bd;
	}
	static String getBrandName(){
		return brandName;
	}
	static void setColor(String cl){
		color=cl;		
	}
	static String getColor(){
		return color;
	}


}